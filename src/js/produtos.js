//Função criar cards de uma categoria
function criarCardProduto(categoria, dados) {
    const grid = document.querySelector(`#${categoria} .produtos`);
    if (!grid) return;

    dados.forEach(item => {
        const card = document.createElement('div');
        card.className = 'produto';

        card.innerHTML = `
            <a href="detalhes-produto.html?id=${item.id}">
                <div class="produto-content">
                    <div class="produto-imagem">
                        <img src="${item.imagem}" alt="${item.nome}">
                    </div>
                    <div class="produto-botao">
                        <button type="button" class="btn-carrinho">Carrinho<span><i
                                    class="fas fa-plus"></i></span></button>
                        <button type="button" class="btn-comprar">Comprar <span><i
                                    class="fas fa-shopping-cart"></i></span></button>
                    </div>
                </div>

                <div class="produto-infos">
                    <div class="infos-top">
                        <h2 class="produto-tipo">${item.tipoCard}</h2>
                        <div class="rating">
                            <span><i class="fas fa-star"></i></span>
                            <span><i class="fas fa-star"></i></span>
                            <span><i class="fas fa-star"></i></span>
                            <span><i class="fas fa-star"></i></span>
                            <span><i class="far fa-star"></i></span>
                        </div>
                    </div>

                    <p class="produto-nome">${item.nome}</p>
                    <div class="produto-preco">
                        <p class="preco">${item.precoOriginal}</p>
                        <p class="preco">${item.precoDesconto}</p>
                    </div>
                </div>

                 <div class="produto-desconto">
                    <h2 class="h2-pd">${item.desconto}</h2>
                </div>
            </a>
        `;

        grid.appendChild(card);
    });
}

// Função inicializar produtos
function inicializarProdutos() {
    criarCardProduto('cachorro', produtosCompleto.cachorro);
    criarCardProduto('gato', produtosCompleto.gato);
    criarCardProduto('coelho', produtosCompleto.coelho);
    criarCardProduto('aves', produtosCompleto.aves);
    criarCardProduto('brinquedos', produtosCompleto.brinquedos);
    criarCardProduto('casinhas', produtosCompleto.casinhas);
    criarCardProduto('petiscos', produtosCompleto.petiscos);
    criarCardProduto('medicamentos', produtosCompleto.medicamentos);
}


